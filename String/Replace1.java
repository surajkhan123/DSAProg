package String;

public class Replace1 {
  public static void main(String[] args) {
    String s1 = "java developer";
    System.out.println(s1.replace('a', '@'));
    System.out.println(s1.replace("java","python"));
    System.out.println(s1.replace("a", ""));
    System.out.println(s1.replace(" ",""));
  }
}
